package com.example.cv.e_commerce.Model;

public class Products {
    private String productName, desc, price, category, image, date, pid, time;

    public  Products(){

    }

    public Products(String productName, String desc, String price, String category, String image, String date, String pid, String time) {
        this.productName = productName;
        this.desc = desc;
        this.price = price;
        this.category = category;
        this.image = image;
        this.date = date;
        this.pid = pid;
        this.time = time;
    }

    public String getProductName() {
        return productName;
    }

    public void setProductName(String productName) {
        this.productName = productName;
    }

    public String getDesc() {
        return desc;
    }

    public void setDesc(String desc) {
        this.desc = desc;
    }

    public String getPrice() {
        return price;
    }

    public void setPrice(String price) {
        this.price = price;
    }

    public String getCategory() {
        return category;
    }

    public void setCategory(String category) {
        this.category = category;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getPid() {
        return pid;
    }

    public void setPid(String pid) {
        this.pid = pid;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }
}
